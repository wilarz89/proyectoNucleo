CREATE DATABASE "Ranking_mundial"
  WITH OWNER = postgres
       ENCODING = 'UTF8'
       TABLESPACE = pg_default
       LC_COLLATE = 'Spanish_Colombia.1252'
       LC_CTYPE = 'Spanish_Colombia.1252'
       CONNECTION LIMIT = -1;

CREATE TABLE "Equipos"
(
  cod_equipo integer NOT NULL,
  nom_equipo text NOT NULL,
  activo_equipo boolean,
  fecha_creacion date,
  CONSTRAINT "Equipos_pkey" PRIMARY KEY (cod_equipo)
)
WITH (
  OIDS=FALSE
);
ALTER TABLE "Equipos"
  OWNER TO postgres;

CREATE TABLE "Resultados"
(
  cod_resultado integer NOT NULL,
  cod_equipo integer NOT NULL,
  enfrentamiento text NOT NULL,
  puntos_partido integer NOT NULL,
  "Goles_Marcados" integer,
  "Goles_Recibidos" integer,
  CONSTRAINT "Resultados_pkey" PRIMARY KEY (cod_resultado),
  CONSTRAINT "Resultados_cod_equipo_fkey" FOREIGN KEY (cod_equipo)
      REFERENCES "Equipos" (cod_equipo) MATCH SIMPLE
      ON UPDATE NO ACTION ON DELETE NO ACTION
)
WITH (
  OIDS=FALSE
);
ALTER TABLE "Resultados"
  OWNER TO postgres;

CREATE TABLE "TipoEquipo"
(
  cod_tipo_equipo numeric NOT NULL,
  nom_tipo_equipo text,
  cod_equipo integer,
  activo_tipo_equipo boolean,
  fecha_creacion date,
  descripcion text,
  CONSTRAINT "TipoEquipo_pkey" PRIMARY KEY (cod_tipo_equipo),
  CONSTRAINT "TipoEquipo_cod_equipo_fkey" FOREIGN KEY (cod_equipo)
      REFERENCES "Equipos" (cod_equipo) MATCH SIMPLE
      ON UPDATE NO ACTION ON DELETE NO ACTION
)
WITH (
  OIDS=FALSE
);
ALTER TABLE "TipoEquipo"
  OWNER TO postgres;

